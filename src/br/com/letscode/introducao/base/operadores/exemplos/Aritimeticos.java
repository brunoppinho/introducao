package br.com.letscode.introducao.base.operadores.exemplos;

/**
 * Operadores + - / * %
 */
public class Aritimeticos {

    public static void main(String[] args) {
        System.out.println("2 + 1 = " + (2 + 1));
        System.out.println("2 - 1 = " + (2 - 1));
        System.out.println("2 * 1 = " + (2 * 1));
        System.out.println("2 / 1 = " + (2 / 1));
        System.out.println("3 % 2 = " + (3 % 2));


    }
}
